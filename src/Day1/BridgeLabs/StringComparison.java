/*
 * This program will compare two strings
 * If both strings are equal then it will give output as true otherwise false 
 */

public class StringComparison {

	public static void main(String[] args) {
		// Variables in string 
		String s1 = "Saravanan";
		String s2 = "Saravanan";
		
		// Using equals() Method
		System.out.println(s1.equals(s2));
	}

}
